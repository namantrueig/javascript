const math = require('./math'); // Import the math module

const num1 = 10;
const num2 = 5;

console.log(`Addition: ${math.add(num1, num2)}`);         // 15
console.log(`Subtraction: ${math.subtract(num1, num2)}`); // 5
console.log(`Multiplication: ${math.multiply(num1, num2)}`); // 50
console.log(`Division: ${math.divide(num1, num2)}`);     // 2
